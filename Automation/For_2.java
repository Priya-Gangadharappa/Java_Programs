package Automation;

public class For_2
{

	public static void main(String[] args)
	{
		for(int i=1;i<4;i++)
		{
			System.out.println("this is second for loop program");
		}
	}
}
/* i=1 ; i<4 ; => true
 *  i+1=2
 *  i=2 ; i<4 ; => true
 *  i+2=3
 *  i=3; i<4 ; => true
 *  i+3=4 
 *  i=4; i<4 ; =>false // loop gets terminated 
 */