package Automation;

public class StaticMethod
{
	static void addition()
	{
		System.out.println("iam your addition method");
	}
	
	static void substraction()
	{
		System.out.println("iam your substraction method");
	}
	
	static void multiplication()
	{
		System.out.println("iam your multiplication method");
	}
	
public static void main(String[] args)
{  
	System.out.println("iam your main method");
	
	addition();
	
	substraction();
	
	multiplication();
	
}


}
