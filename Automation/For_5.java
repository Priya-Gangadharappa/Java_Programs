package Automation;

public class For_5 
{
    public static void main(String[] args)
   {
 	for(int i=5;i>=0;i--)
 	{
 		System.out.println("This is 5th for loop program");
 	}
   }
}
/* i=5;i>=0  => true
5-1=4
i=4;i>=0 => true 
4-1=3
i=3;i>=0 => true
3-1=2
i=2;i>=0 => true
2-1=1
i=1;i>=0 =>true
1-1=0
i=0;i>=0 => true
0-1=-1
-1=0;i>=0 => false for loop gets terminated */