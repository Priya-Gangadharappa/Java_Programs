package Automation;

public class Declaration_initialization_utilization 
{

	
	public static void main(String[] args) 
	{
		
    byte a=18; // declaration and initialization of a variable a with value 18
    a=19;
    System.out.println(a);
    
    
    byte age ; // declaration
    age=19;    // initialization
    age=20;   // updating or reinitializing the local variable
    
    System.out.println(age);
    
    String Name="priya SG";
    System.out.println(Name); //using the variable is called utilization 
    
    Name="priya gangadhar";
    System.out.println(Name);//utilization of String
    
    
	}

}
